module.exports.addTwoNumbers = function(number1, number2) {
  return number1 + number2;
};

module.exports.substractTwoNumbers = function(number1, number2) {
  return number1 - number2;
};

module.exports.multiplyTwoNumbers = function(number1, number2) {
  return number1 * number2;
};

module.exports.divideTwoNumbers = function(number1, number2) {
  return number1 / number2;
};

module.exports.powerOfaNumber = function(number1, number2) {
  return Math.pow(number1, number2);
};
