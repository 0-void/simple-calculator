module.exports.addTwoNumbers = (number1, number2) => {
  return number1 + number2;
};

module.exports.substractTwoNumbers = (number1, number2) => {
  return number1 - number2;
};

module.exports.multiplyTwoNumbers = (number1, number2) => {
  return number1 * number2;
};

module.exports.divideTwoNumbers = (number1, number2) => {
  return number1 / number2;
};

module.exports.powerOfaNumber = (number1, number2) => {
  return Math.pow(number1, number2);
};
